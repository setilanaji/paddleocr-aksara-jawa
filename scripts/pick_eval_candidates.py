"""
pick_eval_candidates.py
Stratifies the data/real/ Leiden pool by source (Or. 1871, Or. 1928, D Or. 15)
and picks a deterministic eval-candidate subset. Writes the chosen filenames
to a plain text file consumed by:

  - scripts/generate_aksara.py --exclude …   (holds back from background pool)
  - Label Studio task filter                 (annotate only these)

Rows are classified by the Leiden manifest item:<id> in sources.csv. The
item_id → source-group mapping is declared in LEIDEN_SOURCE_GROUPS below so
provenance stays legible without re-deriving it from the manifest JSON.

Usage:
    # Default: 120 candidates, stratified 80 Or.1871 / 30 D Or.15 / 10 Or.1928
    uv run python scripts/pick_eval_candidates.py

    # Custom per-source counts + different seed
    uv run python scripts/pick_eval_candidates.py \\
        --or1871 100 --dor15 40 --or1928 20 --seed 7

    # Different output path
    uv run python scripts/pick_eval_candidates.py \\
        --output annotation/eval_candidates_v2.txt
"""

import argparse
import csv
import random
import re
from collections import defaultdict
from pathlib import Path


LEIDEN_SOURCE_GROUPS: dict[str, str] = {
    # Or. 1871 — Panji Jaya Lengkara and Angreni romance (3-volume scan)
    "1987922": "or1871",
    "1988526": "or1871",
    "1989001": "or1871",
    # D Or. 15 — Babad Paku Alaman, illuminated court manuscript
    "2034468": "dor15",
    # Or. 1928 — Kitab pangeran Bonang, dluwang / tree-bark paper
    "1576531": "or1928",
}

SOURCE_DISPLAY: dict[str, str] = {
    "or1871": "Or. 1871 (Panji romance)",
    "dor15":  "D Or. 15 (Babad Paku Alaman)",
    "or1928": "Or. 1928 (Kitab pangeran Bonang)",
}

ITEM_RE = re.compile(r"item:(\d+)/manifest")


def main():
    ap = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    ap.add_argument("--sources-csv", type=Path, default=Path("data/real/sources.csv"))
    ap.add_argument("--output",      type=Path, default=Path("data/eval_real_candidates.txt"))
    ap.add_argument("--or1871",      type=int, default=80,
                    help="candidates from Or. 1871 (640 pages available)")
    ap.add_argument("--dor15",       type=int, default=30,
                    help="candidates from D Or. 15 (118 pages available)")
    ap.add_argument("--or1928",      type=int, default=10,
                    help="candidates from Or. 1928 (42 pages available)")
    ap.add_argument("--seed",        type=int, default=42)
    args = ap.parse_args()

    targets = {"or1871": args.or1871, "dor15": args.dor15, "or1928": args.or1928}
    total_target = sum(targets.values())

    if not args.sources_csv.exists():
        raise SystemExit(f"sources CSV not found: {args.sources_csv}")

    with args.sources_csv.open(newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))

    by_group: dict[str, list[dict]] = defaultdict(list)
    unknown: list[dict] = []
    for r in rows:
        m = ITEM_RE.search(r.get("manifest_url", ""))
        item = m.group(1) if m else None
        group = LEIDEN_SOURCE_GROUPS.get(item) if item else None
        if group is None:
            unknown.append(r)
            continue
        by_group[group].append(r)

    if unknown:
        print(f"WARNING: {len(unknown)} rows could not be classified — skipped")
        for r in unknown[:5]:
            print(f"  {r.get('local_filename')}  manifest={r.get('manifest_url')}")

    rng = random.Random(args.seed)
    picks: list[tuple[str, str]] = []
    for group, want in targets.items():
        pool = by_group.get(group, [])
        if not pool:
            print(f"WARNING: no rows for group {group!r} — skipping")
            continue
        take = min(want, len(pool))
        if take < want:
            print(f"WARNING: group {group!r} wants {want} but only has {len(pool)} — taking all")
        chosen = rng.sample(pool, take)
        for r in chosen:
            picks.append((group, r["local_filename"]))

    picks.sort()

    args.output.parent.mkdir(parents=True, exist_ok=True)
    with args.output.open("w", encoding="utf-8") as f:
        f.write("# Eval-candidate pool for Aksara Jawa OCR\n")
        f.write(f"# Generated by scripts/pick_eval_candidates.py  seed={args.seed}\n")
        f.write(f"# Source CSV: {args.sources_csv}\n")
        f.write("# Per-source targets:\n")
        for g, n in targets.items():
            f.write(f"#   {g:<8} {n:>4}  ({SOURCE_DISPLAY.get(g, g)})\n")
        f.write(f"# Total picked: {len(picks)} / {total_target}\n")
        f.write("#\n")
        f.write("# Every filename here is (1) held back from the semi-synthetic background pool via\n")
        f.write("#   generate_aksara.py --exclude data/eval_real_candidates.txt\n")
        f.write("# and (2) the intended annotation target for the real eval set.\n")
        f.write("# Re-running with the same --seed reproduces this exact list.\n")
        last_group = None
        for group, name in picks:
            if group != last_group:
                f.write(f"\n# --- {SOURCE_DISPLAY.get(group, group)} ---\n")
                last_group = group
            f.write(f"{name}\n")

    print(f"Wrote {len(picks)} candidates to {args.output}")
    for g in targets:
        taken = sum(1 for gg, _ in picks if gg == g)
        avail = len(by_group.get(g, []))
        print(f"  {SOURCE_DISPLAY.get(g, g):<35} {taken:>3} / {avail} available")


if __name__ == "__main__":
    main()
